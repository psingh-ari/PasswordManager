function showStorePass()
{
    var x=document.getElementById("storeForm");
    if(x.style.display==='block')
        x.style.display='none';
    else x.style.display='block';
}

function showViewPass()
{
    var x=document.getElementById("viewTable");
    if(x.style.display==='block')
        x.style.display='none';
    else x.style.display='block';
}